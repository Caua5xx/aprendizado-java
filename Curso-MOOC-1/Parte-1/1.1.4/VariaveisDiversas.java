public class VariaveisDiversas {
    public static void main(String[] args) {
        int frango = 9000;
        double bacon = 0.1;
        String trator = "Zetor";
        System.out.println("Frango: \n" + frango);
        System.out.println("Bacon (kg): \n" + bacon);
        System.out.println("Trator: \n" + trator + "\n");
        System.out.println("E, finalmente, um resumo: \n" + frango + "\n" + bacon + "\n" + trator);
    }
}
